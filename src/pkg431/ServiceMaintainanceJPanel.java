    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        if (_update) {
            Number value = (Number)this._fee.getValue();
            if(value == null)
            {
                value = 0;
            }
            ServiceList.instance().update(_ID,
                    this._name.getText(), value.doubleValue());
            JOptionPane.showMessageDialog(null, "Updated.");
        } else {
            // Create a service
            Number value = (Number)this._fee.getValue();
            if(value == null)
            {
                value = 0;
            }
            Service newService = Factory.getInstance().MakeService(_name.getText(), value.doubleValue());
            // Add Service to the ServiceList
            if (ServiceList.instance().addService(newService)) {
                JOptionPane.showMessageDialog(null, "Added.");
                ResetView();
            }
        }
    }//GEN-LAST:event_AddActionPerformed

