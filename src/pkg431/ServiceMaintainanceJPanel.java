    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        int reply = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete member?", "Delete?", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
            ServiceList.instance().delete(_ID);

            ResetView();
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed
    private void ResetView() {
        _ID = 0;
        _update = false;
        _name.setText(null);
        _fee.setValue(new Double(0.00));
        SetView();
    }

    private void SetView() {
        if (_update) {
            this.Add.setText("Update");
            Service myService = ServiceList.instance().getService(_ID);
            if (null != myService) {
                this._name.setText(myService.getServiceName());
                this._fee.setValue(new Double(myService.getServiceCost()));
            }
        } else {
            this.Add.setText("Add");
        }
        this.DeleteButton.setVisible(_update);
    }

